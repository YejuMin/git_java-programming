package chapter7_반목문의심화_while문.section1_while;

public class 실습7_1_for문을_while문으로 {
    public static void main(String[] args){
        int i;
        i= 0; //초깃값 밖에

        while(i<5){ //초깃값 위로, 증가값 괄호 안으로 뺀 for문을 while로 바꾸고 세미콜론 뺀게 다야
            System.out.println("while문을 공부해요");
            i++; //증가값 아래에
        }
    }
}
